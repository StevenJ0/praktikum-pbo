    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // Pastikan ada item yang dipilih
        if (idList.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus terlebih dahulu", 
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            // Ambil ID dari list yang dipilih
            int selectedId = Integer.parseInt(idList.getSelectedValue());
            
            // Konfirmasi sebelum menghapus
            int confirm = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus data dengan ID " + selectedId + "?", 
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                // Lakukan penghapusan
                service.delete(selectedId);
                
                // Refresh tampilan
                getAllDataButtonActionPerformed(evt);
                
                // Reset index jika tabel kosong
                if (service.isEmpty()) {
                    service.indexReset();
                }
                
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", 
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID tidak valid", 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }  //GEN-LAST:event_deleteButtonActionPerformed

    private void getAllDataButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // Clear existing data in the lists
        id.clear();
        nama.clear();
        
        // Get all Mahasiswa data from service
        List<Mahasiswa> listMahasiswa = service.getAll();
        
        // Populate the lists with data
        for (Mahasiswa mhs : listMahasiswa) {
            id.addElement(String.valueOf(mhs.getId()));
            nama.addElement(mhs.getNama());
        }
    }                                                

