    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {                                         
    // Pastikan ada item yang dipilih
        if (idList.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diupdate terlebih dahulu", 
                "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            // Ambil ID dari list yang dipilih (bukan index list)
            int selectedId = Integer.parseInt(idList.getSelectedValue());
            
            // Ambil data mahasiswa berdasarkan ID
            Mahasiswa dataMahasiswa = service.getById(selectedId);
            if (dataMahasiswa == null) {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan", 
                    "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Update data
            String newNama = textFieldEdit.getText().trim();
            if (newNama.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong", 
                    "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            Mahasiswa updatedMahasiswa = new Mahasiswa(selectedId, newNama);
            service.update(updatedMahasiswa);
            
            // Refresh tampilan
            getAllDataButtonActionPerformed(evt);
            
            // Kosongkan field edit
            textFieldEdit.setText("");
            
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate", 
                "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID tidak valid", 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }                                           

